export const headpieces: Headpiece[] = headpieceConfigs.map(config => ({
  id: config.id,
  name: config.name,
  category: config.category,
  description: config.description,
  suitableRole: config.suitableRole,
  color: config.color,
  svgContent: config.generator({
    mainColor: config.mainColor,
    accentColor: config.accentColor,
    goldColor,
  }),
  defaultScale: config.defaultScale,
  defaultOffsetY: config.defaultOffsetY,
  bottomPosition: { x: HEADPIECE_WIDTH / 2, y: config.bottomY },
}));

export const getHeadpieceById = (id: string): Headpiece | undefined => {
  return headpieces.find(h => h.id === id);
};

export const getHeadpiecesByCategory = (category: HeadpieceCategory): Headpiece[] => {
  return headpieces.filter(h => h.category === category);
};
